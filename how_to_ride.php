<div id="page_text_itself">

<p>The City of Eureka, in cooperation with the county of Humboldt, provides contracted fixed route bus service in the greater Eureka area, known as, Eureka Transit Service (ETS). Eureka Transit Service consists of four (4) routes and operates 60 minute headways, which service different areas of the city encompassing several schools, social services, shopping areas, hospitals, and medical offices as well as major residential areas.</p>

<h3>How To Ride The Bus</h3>

<p>If you are new to our bus system, here are several things you should know before making your trip.

<p><a href="how%20to%20ride%20pdf/rider_guide_ETS-Pubished.pdf">Eureka Transit Rider Guide PDF</a></p>

<p>Simple steps you can follow that help make your trip a safe one:

<ul>
<li>Plan your route ahead of time.
<li>Locate your origin and destination bus stops prior to your fist day of commuting on the bus.
<li>Get to the bus stop at least five minutes early.  If you’re sitting on a bench, stand when you see the bus approaching to signal the driver to stop.
<li>Never walk directly in front of or behind a bus.  The operator and other drivers may not see you.
<li>Check the head sign listed above the windshield to make sure you board the right bus. 
<li>Have your correct fare ready. Drivers cannot take checks and do not carry change.
<li>When the bus arrives, step aboard through the front door.  If you need any assistance with a connecting route, ask the driver.  Once completed, find a comfortable location for your ride.  
<li>In slippery winter conditions, be extra careful getting on and off the bus.
<li>As your stop approaches, ring the chime or pull the signal cord located along the interior windows.  This signals the driver to stop at the next stop.
<li>Exit through the rear door.
</ul>

<h3>Route & Schedule Information</h3>

<p>To get started riding Eureka Transit Service, consult the route map and schedule. The route map and schedule provides four (4) routes, a map highlighting each route by color, streets and cross streets, the minutes past the hour servicing every bus stop, and the hours of operation for each individual route.</p>

<h3>Identifying Your Bus</h3>

<p>Check the head sign listed above the windshield to make sure you board the right bus.   Every Eureka Transit Service Bus is marked beside the front door with the color route and above the windshield with a head sign reading the areas to be serviced.  If you have any doubts or questions about the routes, please ask your driver before boarding the bus.</p>

<h3>Eureka Transit Service Bus Stops</h3>

<p>The physical location of all city bus stops are now listed on the schedule.  The city has designated over 150 bus stops which are generally about four blocks apart.</p>

<h3>Transfers</h3>

<p>Transfers are only good when used to continue any one-way trip origin to destination.  If you need to take more than one bus to make it to your final destination,  please let the driver know ahead of time when paying for your fare. Transfers are free and available from the driver for travel between ETS routes.</p>

<h3>Lost and Found</h3>

<p>Please check your seat before leaving the bus and make sure you have all your belongings.  However, if you do leave something, you may call the HTA office at 707-443-0826.  Every attempt will be made to recover your lost item. Lost and found items can be picked up at 133 "V" Street, Eureka.  Items will be kept up to 30 days, so please pick up your items within that time.</p>

<h3>Wheelchairs</h3>

<p>All of our buses are wheelchair lift equipped.  If you are unable to manage your wheelchair, try to get someone to come with you to help.  For additional assistance or step by step trial instructions for boarding your wheelchair, please contact the HTA office for an appointment.</p>

<h3>Paratransit Services</h3>

<p>Disabled persons unable to use regular bus service may qualify through a certification process for  The City of Eureka Dial-a-Ride Program.  For more information and to obtain an application please contact the Humboldt Transit Authority at 707-443-0826.</p>

<h3>Reduced Fares for Seniors & Disabled</h3>

<p>Reduced fares on fixed route buses are available to senior citizens and disabled persons who have been certified.  Forms are available at the HTA office.  I.D. cards from other systems will be honored.</p>

<h3>On the Bus</h3>
<ul>
<li>Keep the aisle clear of tripping hazards such as bags, backpacks, and briefcases.</li>
<li>Stand back from the front of the bus so the operator has a clear view of the doorway and mirrors.</li>
</ul>

<p>For obvious safety reasons, you cannot go barefoot on the bus, nor wear skates or rollerblades and you must wear a shirt.  Sit where you can see your upcoming stop.</p>

<h3>Children</h3>

<ul>
<li>Take extra care when traveling with young children.  Hold their hand when boarding and exiting.  Keep them seated to avoid falls and bumps, and watch that they don’t put their hands and head out the window.  

<li>Hold strollers securely, put on their brakes and keep the aisle clear, or hold your child and fold the stroller.
</ul>

<h3>We can help</h3>

<ul>
<li>If you feel uncomfortable because of other individuals onboard, or are unsure about stops and bus riding logistics, sit near the front of the bus.</li>

<li>If you’re lost or feel sick, harrassed or threatened, speak to the operator who will help or radio for help.</li>
</ul>

<p>If you have any concerns please call 707-443-0826.</p>



