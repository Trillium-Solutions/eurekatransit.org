<h3>Regional transit passes</h3>
<p>$10 or $20 stored value magnetic media cards that allow you to ride the four regional transit systems for the new discounted rate (<a href="http://www.hta.org/fares.php">see page at HTA website for comprehensive rate table</a>).</p>
<p>Your magnetic pass can be used on any system at any time.  Just swipe the card when you board the bus and it will deduct the correct discounted rate for that system.  Let the driver know if you are entiled to a reduced fare before you board.  See <a href="#purchasing">purchasing passes, below</a>.</p>

<h3>Month passes</h3>
<p>Month Passes are valid for unlimited rides until the date and time of expiration printed on the back of the pass. Each time you wish to use the pass after purchasing it, simply swipe it through the card reader (see diagram). When you hear a beep the pass is recognized as valid and you may proceed to board the bus.  See <a href="#purchasing">purchasing passes, below</a>.</p>

<p>Passes issued before the introduction of the new fareboxes will be honored until their expiration date. The fareboxes will not affect the HSU Jack Pass.</p>

<h3>Transfers</h3>
<p>Free transfers are provided between ETS buses for a single trip.  No inter-agency transfers are available.</p>

<p>Tell the driver you want a transfer when you board the bus.  You can only get a transfer upon boarding.</p>

<p>To use a transfer when you board a bus on another route simply insert it into the card reader. When you hear a beep the transfer is recognized as valid and you may proceed to board the bus.</p>

<a name="purchasing"></a><h2>Purchasing passes</h2>
<p>You can purchase passes from the bus driver using cash. <em><strong>Important note:</strong></em> If you are not paying a cash fare do not put your money in the farebox until you tell the driver what you want to buy. You will not be refunded for money placed in farebox accidently.</p>
<p>Passes can also be purchased at the <a href="http://maps.google.com/maps?q=humboldt+transit+authority&ie=UTF8&oe=utf-8&client=firefox-a&ll=40.806598,-124.149756&spn=0.02537,0.023088&z=15&iwloc=A&om=1">Humboldt Transit Authority office at 133 V Street in Eureka</a> using Visa, Mastercard, or a personal check.</p>

<h2>Using the new fareboxes</h2>
<p>Have your exact fare ready to insert into the farebox <strong><em>before</em></strong> boarding the bus. Neither the driver nor the new farebox can make change or refund money put into the farebox on accident.</p>
<p><strong>Paper money (bills)</strong> - Feed unfolded bills into the bill slot. The farebox will not accept bills larger than $20.</p>
<p><strong>Coins</strong> - Drop coins, one at a time, into the coin slot.  Coins accepted are: pennies, nickels, dimes, quarters, halves & small dollar coins.  Note: pennies will no longer be accepted after April 26th.</p>
<p>A digital display shows the exact amount deposited. When you hear a beep you have paid the correct fare and may proceed to board the bus.  If you are entitled to a reduced cash fare be sure to tell the driver before you place your money in the farebox.</p>

<h3>Questions?</h3>
<p>If you have any questions call Humboldt Transit Authority at 443-0826.</p>


<h2>Jack Pass</h2>
<p><i><strong>Humboldt State University students ride RTS free</strong></i></p>
<p>Students: Just show and swipe your current student ID when you board.  Staff & faculty: you can buy into the Jack Pass program for $60 per semester.  Get <a href="http://studentaffairs.humboldt.edu/parking/bus_info.php">more info from HSU Parking & Commuter Services</a>, and learn more about the development of <a href="http://www.green-wheels.org/taxonomy/term/53">Jack Pass at the Green Wheels website</a>.</p>




